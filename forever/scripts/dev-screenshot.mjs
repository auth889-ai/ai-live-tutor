import { chromium } from 'playwright';
const browser = await chromium.launch();
const page = await browser.newPage({ viewport: { width: 1500, height: 1050 } });
await page.goto('http://localhost:3000/dev/lenses', { waitUntil: 'networkidle', timeout: 90000 }).catch(() => {});
await page.waitForTimeout(5000);
const dij = page.locator('h2', { hasText: 'dijkstra' }).first();
await dij.scrollIntoViewIfNeeded();
await page.waitForTimeout(1500);
await page.screenshot({ path: '/tmp/shot-dijkstra.png' });
const lcs = page.locator('h2', { hasText: 'LCS' }).first();
await lcs.scrollIntoViewIfNeeded();
await page.waitForTimeout(1500);
await page.screenshot({ path: '/tmp/shot-lcs.png' });
console.log('dijkstra + lcs shots');
await browser.close();
