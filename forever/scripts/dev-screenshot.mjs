import { chromium } from 'playwright';
const browser = await chromium.launch();
const page = await browser.newPage({ viewport: { width: 1500, height: 1000 } });
await page.goto('http://localhost:3000/dev/lenses', { waitUntil: 'networkidle', timeout: 90000 }).catch(() => {});
await page.waitForTimeout(5000);
await page.screenshot({ path: '/tmp/shot-lenses-top.png' });
await page.evaluate(() => window.scrollTo(0, document.body.scrollHeight * 0.4));
await page.waitForTimeout(1500);
await page.screenshot({ path: '/tmp/shot-lenses-mid.png' });
console.log('gallery shots taken');
await browser.close();
